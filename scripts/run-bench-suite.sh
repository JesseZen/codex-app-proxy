#!/bin/bash
set -euo pipefail

RESULTS_DIR="/tmp/codex-bench-results"
rm -rf "$RESULTS_DIR"
mkdir -p "$RESULTS_DIR"

run_bench() {
  local name="$1"
  shift
  echo "=== Running: $name ==="
  env "$@" node scripts/benchmark.js > "$RESULTS_DIR/$name.json" 2>&1
  echo "--- Done: $name ---"
  cat "$RESULTS_DIR/$name.json"
  echo ""
  echo ""
}

# A组: 请求body大小 (0, 1K, 32K, 256K, 1M), 固定并发40, 无延迟
run_bench "A1_body_0"     BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_BODY_BYTES=0
run_bench "A2_body_1K"    BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_BODY_BYTES=1024
run_bench "A3_body_32K"   BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_BODY_BYTES=32768
run_bench "A4_body_256K"  BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_BODY_BYTES=262144
run_bench "A5_body_1M"    BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_BODY_BYTES=1048576

# B组: 响应大小 (256, 1K, 64K, 256K), 固定并发40, 无延迟
run_bench "B1_resp_256"   BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_RESPONSE_BYTES=256
run_bench "B2_resp_1K"    BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_RESPONSE_BYTES=1024
run_bench "B3_resp_64K"   BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_RESPONSE_BYTES=65536
run_bench "B4_resp_256K"  BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_RESPONSE_BYTES=262144

# C组: 上游延迟 (0, 50, 200, 500ms), 固定并发40
run_bench "C1_delay_0"    BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_UPSTREAM_DELAY_MS=0
run_bench "C2_delay_50"   BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_UPSTREAM_DELAY_MS=50
run_bench "C3_delay_200"  BENCH_REQUESTS=400 BENCH_CONCURRENCY=40 BENCH_UPSTREAM_DELAY_MS=200
run_bench "C4_delay_500"  BENCH_REQUESTS=200 BENCH_CONCURRENCY=40 BENCH_UPSTREAM_DELAY_MS=500

# D组: 并发度 (1, 10, 50, 100, 200), 固定body 32K
run_bench "D1_conc_1"     BENCH_REQUESTS=200 BENCH_CONCURRENCY=1
run_bench "D2_conc_10"    BENCH_REQUESTS=400 BENCH_CONCURRENCY=10
run_bench "D3_conc_50"    BENCH_REQUESTS=400 BENCH_CONCURRENCY=50
run_bench "D4_conc_100"   BENCH_REQUESTS=400 BENCH_CONCURRENCY=100
run_bench "D5_conc_200"   BENCH_REQUESTS=400 BENCH_CONCURRENCY=200

echo "========== ALL BENCHMARKS COMPLETE =========="
